    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        // TODO add your handling code here:
        String CUSID = Customer_ID.getText();
        String hoTen = Customer_name.getText();
        String diaChi = Address.getText();
        String soDienThoai = Phone.getText();
        String email = Email.getText();

        // Kiểm tra xem có ô nào đó trống không
        if (CUSID.isEmpty() || hoTen.isEmpty() || diaChi.isEmpty() || soDienThoai.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin.");
            return;
        }

        // Kiểm tra dữ liệu nếu cần thiết
        if (kiemTraTrungMaKhachHang(CUSID)) {
            JOptionPane.showMessageDialog(this, "Mã đã tồn tại trong danh sách. Vui lòng chọn mã khác.");
            return;
        }

        // Tạo một đối tượng KhachHang mới
        Customer khachHangMoi = new Customer(CUSID, hoTen, diaChi, soDienThoai, email);
    
        // Gọi phương thức DAO để thêm khách hàng vào cơ sở dữ liệu
        customerDAO.addCustomer(khachHangMoi);

        // Cập nhật bảng để hiển thị dữ liệu đã cập nhật
        UpdateActionPerformed(evt);

        JOptionPane.showMessageDialog(this, "Thêm khách hàng thành công!");
        clearTextFields();
    }//GEN-LAST:event_AddActionPerformed

