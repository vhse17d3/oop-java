    private void EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditActionPerformed
          // Lấy thông tin khách hàng từ text fields
        // Lấy thông tin khách hàng từ text fields
        String CUSID = Customer_ID.getText();
        String hoTen = Customer_name.getText();
        String diaChi = Address.getText();
        String soDienThoai = Phone.getText();
        String email = Email.getText();

    // Kiểm tra xem có bỏ trống ID không
        if (CUSID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã khách hàng không được để trống.");
            return;
        }
        if (!kiemTraTrungMaKhachHang(CUSID)) {
            JOptionPane.showMessageDialog(this, "Mã khách hàng không được sửa.");
            return;
        }

        // Kiểm tra xem ID có trùng với bất kỳ khách hàng nào trong danh sách không
            // Tiếp tục cập nhật thông tin khách hàng
            // Tạo một đối tượng KhachHang mới
        Customer khachHangCapNhat = new Customer(CUSID, hoTen, diaChi, soDienThoai, email);

        // Gọi phương thức DAO để cập nhật khách hàng trong cơ sở dữ liệu
        customerDAO.updateCustomer(khachHangCapNhat);
        JOptionPane.showMessageDialog(this, "cập nhật khách hàng thành công!");
        clearTextFields();
        UpdateActionPerformed(evt);
    }//GEN-LAST:event_EditActionPerformed

    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        // Lấy giá trị từ JTextField txtCustomerID
        String CUSID = Customer_ID.getText();

        // Kiểm tra xem CUSID có giá trị hay không
        if (!CUSID.isEmpty()) {
        // Gọi phương thức deleteCustomer của đối tượng customerDAO để xóa khách hàng từ cơ sở dữ liệu
        customerDAO.deleteCustomer(CUSID);
        JOptionPane.showMessageDialog(this, "xóa khách hàng thành công!");
        // Làm mới dữ liệu trên bảng để hiển thị danh sách khách hàng đã được cập nhật
        clearTextFields();
        UpdateActionPerformed(evt);
    
    } else {
        // Hiển thị thông báo lỗi nếu CUSID trống
        JOptionPane.showMessageDialog(this, "Mã khách hàng không được để trống.");
    }

    }//GEN-LAST:event_DeleteActionPerformed

    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        // TODO add your handling code here:
    
        // Lấy danh sách tất cả khách hàng từ cơ sở dữ liệu
        List<Customer> customers = customerDAO.getAllCustomers();

        // Tạo một DefaultTableModel cho jTable1
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    
        // Xóa các hàng hiện tại từ bảng
        model.setRowCount(0);

        // Điền bảng với dữ liệu từ danh sách khách hàng
        for (Customer customer : customers) {
            Object[] row = {
                customer.getCUSID(),
                customer.getHoTen(),
                customer.getDiaChi(),
                customer.getSoDienThoai(),
                customer.getEmail()
            };
            model.addRow(row);
        }
    }//GEN-LAST:event_UpdateActionPerformed

