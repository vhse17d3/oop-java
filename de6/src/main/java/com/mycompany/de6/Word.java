package com.mycompany.de6;

/**
 *
 * @author sonki
 */
public class Word {

    public Word(int id, String en, String vn) {
        this.id = id;
        this.en = en;
        this.vn = vn;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEn() {
        return en;
    }

    public void setEn(String en) {
        this.en = en;
    }

    public String getVn() {
        return vn;
    }

    public void setVn(String vn) {
        this.vn = vn;
    }

    @Override
    public String toString() {
        return  "id=" + id + ", en=" + en + ", vn=" + vn ;
    }
    private int id;
    private String en;
    private String vn;
}
