    private void updateBookList() {
        // Đọc danh sách sách từ file khi cần cập nhật
        bookManager.loadFromFile("books.dat");
        // Lấy danh sách ID sách từ BookManager
        Set<String> bookIds = bookManager.getAllBookIds();

        // Chuyển danh sách ID thành mảng
        String[] bookIdArray = bookIds.toArray(new String[0]);

        // Thiết lập mô hình cho JList
        List_id_book.setModel(new javax.swing.AbstractListModel<String>() {
            public int getSize() {
                return bookIdArray.length;
            }

            public String getElementAt(int i) {
                return bookIdArray[i];
            }
        });
    }
    
