    private void showBookDetails() {
        // Lấy id sách được chọn từ JList
        String selectedBookId = List_id_book.getSelectedValue();

        // Kiểm tra xem có sách được chọn hay không
        if (selectedBookId != null) {
            // Sử dụng BookManager để lấy thông tin sách
            Book selectedBook = bookManager.getBookById(selectedBookId);

            // Hiển thị thông tin sách trên các JTextField tương ứng
            Authors_show.setText(selectedBook.getAuthor());
            Title_show.setText(selectedBook.getTitle());
            Date_show.setText(selectedBook.getPublicationDate());
            Categories_show.setText(selectedBook.getCategories());
        }
    }
    
